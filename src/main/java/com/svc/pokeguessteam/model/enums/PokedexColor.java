package com.svc.pokeguessteam.model.enums;

/**
 * Categorias de cor da Pokédex (desde a 3ª geração): aparência dominante.
 * Pokémon alaranjados costumam ser classificados como {@link #RED} ou {@link #BROWN}.
 */
public enum PokedexColor {
    RED,
    BLUE,
    YELLOW,
    GREEN,
    BLACK,
    BROWN,
    PURPLE,
    GRAY,
    WHITE,
    PINK
}
